// Helper functions
export const getCategoriesByDomain = (domain) => {
  return allCategories[domain] || [];
};

export const findCategoryByName = (domain, categoryName) => {
  const domainCategories = getCategoriesByDomain(domain);
  return domainCategories.find(category =>
    category.name.toLowerCase() === categoryName.toLowerCase()
  );
};

export const findCategoryById = (domain, categoryId) => {
  const domainCategories = getCategoriesByDomain(domain);
  return domainCategories.find(category => category.id === categoryId);
};

export const getAllCategoryNames = (domain) => {
  const domainCategories = getCategoriesByDomain(domain);
  return domainCategories.map(category => category.name);
};
